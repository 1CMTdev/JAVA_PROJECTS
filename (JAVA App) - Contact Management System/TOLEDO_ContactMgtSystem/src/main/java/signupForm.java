 public boolean isUsernameExist(String un){
        
        boolean uExist = false;
        Connection con = myConnection.getConnection();
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = con.prepareStatement("SELECT * FROM `user` WHERE `username` = ? ");
            ps.setString(1,jTextFieldUsername1.getText());

            rs = ps.executeQuery();
            
            if(rs.next()){
                uExist = true;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return uExist;
    }
 public boolean verifData(){
        
        //if [username - first name - last name - password] are empty
        if(jTextFieldFirstName.getText().equals("")&&jTextFieldLastName.getText().equals("")
            || jTextFieldUsername1.getText().equals("") 
                || String.valueOf(jPasswordField1create.getPassword()).equals(""))
        {
            JOptionPane.showMessageDialog(null, "One or More Field are empty");
            return false;
        }
        
        //if pass1 are different from pass2
        else if(!String.valueOf(jPasswordField1create.getPassword()).equals(String.valueOf(jPasswordField2create.getPassword()))){
            JOptionPane.showMessageDialog(null, "Incorrect Password");
            return false;
        }
        
        //if no image was selected
        else if(imagepath == null)
        {
            JOptionPane.showMessageDialog(null, "No Image Selected");
            return false;
        }
        
        //else everything is ok
        else{
            return true;
        }
    }
 
