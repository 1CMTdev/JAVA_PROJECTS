    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here:
        if(verifData())
        {
            Connection con = myConnection.getConnection();
        PreparedStatement ps;
        
        try {
            ps = con.prepareStatement("INSERT INTO `user`(`fname`, `lname`, `username`, `pass`, `pic`) VALUES (?,?,?,?,?)");
            ps.setString(1, jTextFieldFirstName.getText());
            ps.setString(2, jTextFieldLastName.getText());
            ps.setString(3, jTextFieldUsername1.getText());
            ps.setString(4, String.valueOf(jPasswordField2create.getPassword()));
            
            InputStream img = new FileInputStream(new File(imagepath));
            ps.setBlob(5, img);
            
            if(isUsernameExist(jTextFieldUsername1.getText())){
                JOptionPane.showMessageDialog(null, "Username Already Exist");
            }
            else{
                if(ps.executeUpdate() != 0){
                JOptionPane.showMessageDialog(null, "Account Created");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Something Wrong");
                }
                
            }
            
            
            
        } catch (Exception ex) {
            Logger.getLogger(signupForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
        
        
    }//GEN-LAST:event_jButtonCreateActionPerformed

